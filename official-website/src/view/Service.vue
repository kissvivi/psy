                    name: 'xxx',
                    details: 'Customize App',
                    avatar: require('@/assets/img/psy3.png')
                }
            ]
        }
    },
    mounted(){
        var wow = new WOW();
        wow.init();
    },
    created() {
        this.initDoctor();
    },
    methods:{

        initDoctor () {
        listDoctor(this.listQuery)
        .then((response) => {
          this.doctorList = response.data.list;
          console.log("response", response.data);
        })
        .catch((res) => {
          this.$message.error("加载医生信息失败");
        });
        },

        ServiceClick(id){
            this.$router.push({
                name: 'servicedetail',
                params: {
                    id: id
                }
            })
        }
    }
}
